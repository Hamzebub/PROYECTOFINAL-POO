    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        obtenerIDxTabla();
        Categoria _marca = lista.stream().filter(m -> m.getCategoria_Id()==ID).findFirst()
        .orElse(null);
        if(_marca==null){
            JOptionPane.showMessageDialog(null, "No hay registros seleccionados");
            return;
        }
        txtCategoriaDetalle.setText(_marca.getCategoria_Detalle());
        activarBotones(true);
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        obtenerIDxTabla();
        if(ID==0){
            JOptionPane.showMessageDialog(null, "No hay registros seleccionados");
            return;
        }
        if( JOptionPane.showConfirmDialog(null, "Desea eliminar registro?")!=JOptionPane.YES_OPTION){
            return;
        }

        Respuesta r = obj.eliminar(ID);
        JOptionPane.showMessageDialog(null, r.getMensaje());
        if(r.esCorrecto()){
            cargarTabla();
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

