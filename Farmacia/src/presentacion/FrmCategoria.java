    private void Guardar(){
        Categoria m = new Categoria(ID,txtCategoriaDetalle.getText());
        Respuesta r= new Respuesta();
        if(m.getCategoria_Id()==0){
            r = obj.guardar(m);
        }else{
            r = obj.actualizar(m);
        }
        
        JOptionPane.showMessageDialog(null, r.getMensaje());
        if(r.esCorrecto()){
            cargarTabla();
            activarBotones(false);
            txtCategoriaDetalle.setText("");
        }
        
    }
    
    private void cargarTabla() {

    Respuesta<List<Categoria>> r = obj.listar();
    

    modelo.setRowCount(0);

    if (r.getEstado() == EstadoOperacion.EXITO) {
        lista = r.getDatos();
        for (Categoria m : r.getDatos()) {
            modelo.addRow(new Object[]{
                m.getCategoria_Id(),
                m.getCategoria_Detalle()
            });
        }
    } else {
        JOptionPane.showMessageDialog(this, r.getMensaje());
    }
    }
    
    private void obtenerIDxTabla(){
        
        int fila= tblDatos.getSelectedRow();
        ID =Integer.parseInt(tblDatos.getValueAt(fila, 0).toString());
    }
    
